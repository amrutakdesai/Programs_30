package All_Pragrams_30;

public class _19_Print_AtoZ {
	public static void main(String ar[])
	{
		System.out.println("------------------All Alphabates a To z------------------");

		for(char i='a';i<='z';i++)
		{
			System.out.print(" "+i+" ");
		}
		System.out.println("\n ");
		System.out.println("------------------All Alphabates A To Z------------------");

		for(char i='A';i<='Z';i++)
		{
			System.out.print(" "+i+" ");
		}
	}

}
