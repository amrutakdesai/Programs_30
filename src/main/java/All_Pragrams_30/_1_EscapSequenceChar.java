//1. Escape characters in java

package All_Pragrams_30;

public class _1_EscapSequenceChar 
{

	public static void main(String ar[])
	{
		//Escap Sequence Charecter
		// \t->Horizontal tab 
		System.out.println("\t hello Everyone");

		// \n->new line  
		System.out.println("hello Everyone \n WELCOME TO HOME");

		// \b-> Backspace
		System.out.println("\b hello Everyone");
		
		// \r->insert the carrige return 
		System.out.println("\r hello Everyone");
		
		// \"->insert the Double Quots("") in print stmt
		System.out.println("\" hello Everyone\"");
		
		// \'->insert the quets('') in print stmt
		System.out.println("\' hello Everyone\'");

		// \\->insert the slash in print stmt 
		System.out.println("\\ hello Everyone /");

	}
}